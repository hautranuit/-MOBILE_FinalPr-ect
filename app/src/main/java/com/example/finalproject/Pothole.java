package com.example.finalproject;

public class Pothole {
    private String date;
    private int count;

    // Constructor
    public Pothole() {
    }

    // Getter
    public String getDate() {
        return date;
    }

    public int getCount() {
        return count;
    }

    // Setter
    public void setDate(String date) {
        this.date = date;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
